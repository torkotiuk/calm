// >, <, =, == ---- comparison operators
// === ---- important comparison operators
// 1, 10, 152 ------ operands
console.log(5 == 2);
// give us 'false' in console
// JS likes to fix constants to necessary one,
// f.e. JS could change 'string' to 'number'
// f.e. console.log('5' == 5); works,
// but it's stupid, b/c string is NOT number
// Ex.1-A - much correctly for JS
console.log('5' === 5);
// give us 'false', and it's ok, so we have to use it
// or
console.log(true === 1);
